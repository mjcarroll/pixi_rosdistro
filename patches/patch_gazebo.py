import os
import re

def get_major_version(cmakelists_path):
    if not os.path.exists(cmakelists_path):
        return None
    with open(cmakelists_path, "r") as f:
        for line in f:
            match = re.search(r"set\(LIB_VER_MAJOR (\d+)\)", line)
            if match:
                return match.group(1)
    return None

def patch_gz_vendor(package_path):
    cmakelists_path = os.path.join(package_path, "CMakeLists.txt")
    if not os.path.exists(cmakelists_path):
        print(f"Skipping {package_path}, no CMakeLists.txt found.")
        return

    major_version = get_major_version(cmakelists_path)
    pkg_name = os.path.basename(package_path)
    pkg_basename = pkg_name.replace("_vendor", "").replace("_", "-")
    
    if major_version:
        versioned_name = f"{pkg_basename}{major_version}"
    else:
        versioned_name = pkg_basename
    
    unversioned_name = pkg_basename

    with open(cmakelists_path, "r") as f:
        content = f.read()

    # 1. Add ament_export_dependencies
    if "ament_export_dependencies" not in content:
        export_dep = f"\nament_export_dependencies({versioned_name} {unversioned_name})\n"
        content = content.replace("ament_package()", export_dep + "ament_package()")
    elif versioned_name not in content:
        pattern = r"(ament_export_dependencies\()(.+?)(\))"
        replacement = rf"\1\2 {versioned_name} {unversioned_name}\3"
        content = re.sub(pattern, replacement, content)

    # 2. Add CONFIG_EXTRAS
    extras_file = f"{pkg_name}-extras.cmake.in"
    if "CONFIG_EXTRAS" not in content:
        content = content.replace("ament_package()", f"ament_package(\n  CONFIG_EXTRAS \"{extras_file}\"\n)")

    # 3. Windows fix: Use Embedded PDBs for vendored builds to avoid EBUSY/C1041
    if "VCS_URL" in content and "CMAKE_ARGS" in content and "CMAKE_MSVC_DEBUG_INFORMATION_FORMAT" not in content:
        content = content.replace(
            "CMAKE_ARGS",
            "CMAKE_ARGS\n      -DCMAKE_MSVC_DEBUG_INFORMATION_FORMAT=Embedded"
        )
        print(f"Added Embedded PDB fix to {cmakelists_path}")

    with open(cmakelists_path, "wb") as f:
        f.write(content.encode('utf-8').replace(b'\r\n', b'\n'))

    # 4. Create extras.cmake.in
    extras_content = f"""# Generated by patch_gazebo.py
find_package(ament_cmake_core REQUIRED)
set(@PROJECT_NAME@_PREFIX "${{@PROJECT_NAME@_DIR}}/../../../opt/@PROJECT_NAME@")

# Add the vendored package to the prefix path
list(APPEND CMAKE_PREFIX_PATH "${{@PROJECT_NAME@_PREFIX}}")
list(APPEND CMAKE_PREFIX_PATH "${{@PROJECT_NAME@_PREFIX}}/share")

if(WIN32)
  if(COMMAND ament_prepend_variable_by_name)
    ament_prepend_variable_by_name(PATH "${{@PROJECT_NAME@_PREFIX}}/bin")
  else()
    set(ENV{{PATH}} "${{@PROJECT_NAME@_PREFIX}}/bin;$ENV{{PATH}}")
  endif()
endif()

# Create relay files to help find_package find the nested Gazebo config files
set(_relay_dir "${{@PROJECT_NAME@_DIR}}")
set(_gz_config_dir "${{@PROJECT_NAME@_PREFIX}}/share/cmake/{pkg_basename}")

macro(create_relay_file _name _target_dir)
  if(NOT EXISTS "${{_relay_dir}}/${{_name}}Config.cmake")
    file(TO_CMAKE_PATH "${{_target_dir}}" _cmake_target_dir)
    file(WRITE "${{_relay_dir}}/${{_name}}Config.cmake"
      "set(${{_name}}_DIR \\"${{_cmake_target_dir}}\\")\\n"
      "find_package(${{_name}} REQUIRED NO_DEFAULT_PATH)\\n"
    )
  endif()
endmacro()

if(EXISTS "${{_gz_config_dir}}")
  create_relay_file("{versioned_name}" "${{_gz_config_dir}}")
  create_relay_file("{unversioned_name}" "${{_gz_config_dir}}")
endif()

if(EXISTS "${{_gz_config_dir}}/cmake{major_version if major_version else ""}")
  create_relay_file("{versioned_name}" "${{_gz_config_dir}}/cmake{major_version if major_version else ""}")
endif()

# Fallback aliasing
if(NOT {versioned_name}_FOUND)
    find_package({unversioned_name} QUIET)
    if({unversioned_name}_FOUND)
        set({versioned_name}_FOUND ON)
        set({versioned_name}_INCLUDE_DIRS ${{{unversioned_name}_INCLUDE_DIRS}})
        set({versioned_name}_LIBRARIES ${{{unversioned_name}_LIBRARIES}})
    endif()
endif()
"""
    extras_path = os.path.join(package_path, extras_file)
    with open(extras_path, "wb") as f:
        f.write(extras_content.encode('utf-8').replace(b'\r\n', b'\n'))
    print(f"Patched {pkg_name}")

if __name__ == "__main__":
    base_path = "src/gazebo-release"
    if os.path.exists(base_path):
        for pkg in os.listdir(base_path):
            full_path = os.path.join(base_path, pkg)
            if os.path.isdir(full_path) and pkg.endswith("_vendor"):
                patch_gz_vendor(full_path)
