import os
import re

def get_major_version(cmakelists_path):
    if not os.path.exists(cmakelists_path):
        return None
    with open(cmakelists_path, "r") as f:
        for line in f:
            match = re.search(r"set\(LIB_VER_MAJOR (\d+)\)", line)
            if match:
                return match.group(1)
    return None

def patch_gz_vendor(package_path):
    cmakelists_path = os.path.join(package_path, "CMakeLists.txt")
    if not os.path.exists(cmakelists_path):
        print(f"Skipping {package_path}, no CMakeLists.txt found.")
        return

    major_version = get_major_version(cmakelists_path)
    if not major_version:
        print(f"Could not determine major version for {package_path}")
        return

    # Determine names (e.g., gz-cmake5 and gz-cmake)
    pkg_basename = os.path.basename(package_path).replace("_vendor", "").replace("_", "-")
    versioned_name = f"{pkg_basename}{major_version}"
    unversioned_name = pkg_basename

    with open(cmakelists_path, "r") as f:
        content = f.read()

    # Add ament_export_dependencies if missing
    if "ament_export_dependencies" not in content:
        # Export both to be safe
        export_dep = f"\nament_export_dependencies({versioned_name} {unversioned_name})\n"
        content = content.replace("ament_package()", export_dep + "ament_package()")
        print(f"Adding ament_export_dependencies to {cmakelists_path}")

    # Convert any existing CONFIG_EXTRAS_POST to CONFIG_EXTRAS
    if "CONFIG_EXTRAS_POST" in content:
        content = content.replace("CONFIG_EXTRAS_POST", "CONFIG_EXTRAS")
        print(f"Changed CONFIG_EXTRAS_POST to CONFIG_EXTRAS in {cmakelists_path}")

    # Add CONFIG_EXTRAS to ament_package if missing
    extras_file = f"{os.path.basename(package_path)}-extras.cmake.in"
    if "CONFIG_EXTRAS" not in content:
        content = content.replace("ament_package()", f"ament_package(\n  CONFIG_EXTRAS \"{extras_file}\"\n)")
        print(f"Adding CONFIG_EXTRAS to {cmakelists_path}")

    with open(cmakelists_path, "wb") as f:
        f.write(content.encode('utf-8').replace(b'\r\n', b'\n'))

    # Create the extras file
    extras_path = os.path.join(package_path, extras_file)
    extras_content = f"""# Generated by patch_gazebo.py
find_package(ament_cmake_core REQUIRED)
set(@PROJECT_NAME@_PREFIX "${{@PROJECT_NAME@_DIR}}/../../../opt/@PROJECT_NAME@")
# Broaden fallback search for Gazebo-style directory structures
list(APPEND CMAKE_PREFIX_PATH "${{@PROJECT_NAME@_PREFIX}}")
list(APPEND CMAKE_PREFIX_PATH "${{@PROJECT_NAME@_PREFIX}}/share/gz")
list(APPEND CMAKE_PREFIX_PATH "${{@PROJECT_NAME@_PREFIX}}/lib/cmake")
"""
    with open(extras_path, "wb") as f:
        f.write(extras_content.encode('utf-8').replace(b'\r\n', b'\n'))
    print(f"Created/Updated {extras_path}")

if __name__ == "__main__":
    base_path = "src/gazebo-release"
    if not os.path.exists(base_path):
        print(f"Base path {base_path} not found.")
    else:
        for pkg in os.listdir(base_path):
            full_path = os.path.join(base_path, pkg)
            if os.path.isdir(full_path) and pkg.endswith("_vendor"):
                patch_gz_vendor(full_path)
