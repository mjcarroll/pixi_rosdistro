import os
import re
import subprocess
import sys

def git_reset(path):
    pkg_dir = os.path.dirname(path)
    while pkg_dir and not os.path.exists(os.path.join(pkg_dir, '.git')):
        next_dir = os.path.dirname(pkg_dir)
        if next_dir == pkg_dir:
            pkg_dir = None
            break
        pkg_dir = next_dir
    
    if pkg_dir:
        rel_path = os.path.relpath(path, pkg_dir)
        subprocess.run(["git", "checkout", "--", rel_path], cwd=pkg_dir, capture_output=True)

def patch_gz_vendor(package_path):
    cmakelists_path = os.path.join(package_path, "CMakeLists.txt")
    if not os.path.exists(cmakelists_path):
        return

    git_reset(cmakelists_path)

    pkg_name = os.path.basename(package_path)
    pkg_basename = pkg_name.replace("_vendor", "").replace("_", "-")
    
    with open(cmakelists_path, "r") as f:
        content = f.read()
        match = re.search(r"set\(LIB_VER_MAJOR (\d+)\)", content)
        if match:
            major_version = match.group(1)
        else:
            if "cmake" in pkg_name: major_version = "3"
            elif "math" in pkg_name: major_version = "7"
            else: major_version = ""

    versioned_name = f"{pkg_basename}{major_version}"
    unversioned_name = pkg_basename

    # 1. Add ament_export_dependencies
    # We want to export the names that downstream packages will try to find_package
    export_dep = f"ament_export_dependencies({versioned_name} {unversioned_name})\n"
    if "ament_export_dependencies" not in content:
        content = content.replace("ament_package()", export_dep + "ament_package()")
    
    # 2. Add CONFIG_EXTRAS
    # We'll add our relay files to CONFIG_EXTRAS
    # ament_package will automatically process .in files
    relay_v = f"{versioned_name}Config.cmake.in"
    relay_u = f"{unversioned_name}Config.cmake.in"
    
    if "CONFIG_EXTRAS" not in content:
        content = content.replace("ament_package()", f"ament_package(CONFIG_EXTRAS {relay_v} {relay_u})")
    else:
        # Append to existing extras
        content = re.sub(r"CONFIG_EXTRAS\s+(.+?)\)", f"CONFIG_EXTRAS \\1 {relay_v} {relay_u})", content)

    with open(cmakelists_path, "w") as f:
        f.write(content)

    # 3. Create the relay .in files
    # These files will be processed by ament_package
    # We use @PROJECT_NAME@ because it's standard.
    def create_relay_in(name):
        lines = [
            f"# Generated by patch_gazebo.py for {name}",
            # ament_package sets @PROJECT_NAME@_DIR to the installed cmake directory
            "set(@PROJECT_NAME@_OPT_DIR \"${@PROJECT_NAME@_DIR}/../../../opt/@PROJECT_NAME@\")",
            "list(APPEND CMAKE_PREFIX_PATH \"${@PROJECT_NAME@_OPT_DIR}\")",
            "",
            # Help find the actual config which might be in lib/cmake or share/cmake
            "set(" + name + "_DIR \"${@PROJECT_NAME@_OPT_DIR}/lib/cmake/" + unversioned_name + "\")",
            "if(NOT EXISTS \"${" + name + "_DIR}\")",
            "  set(" + name + "_DIR \"${@PROJECT_NAME@_OPT_DIR}/share/cmake/" + unversioned_name + "\")",
            "endif()",
            "",
            "find_package(" + name + " REQUIRED NO_DEFAULT_PATH)",
            "",
            "# Create aliases for downstream",
            "if(" + name + "_FOUND)",
            "  if(NOT TARGET " + unversioned_name + "::core AND TARGET " + versioned_name + "::" + versioned_name + ")",
            "    add_library(" + unversioned_name + "::core ALIAS " + versioned_name + "::" + versioned_name + ")",
            "  endif()",
            "  if(NOT TARGET " + unversioned_name + "::" + unversioned_name + " AND TARGET " + versioned_name + "::" + versioned_name + ")",
            "    add_library(" + unversioned_name + "::" + unversioned_name + " ALIAS " + versioned_name + "::" + versioned_name + ")",
            "  endif()",
            "endif()"
        ]
        content = "\n".join(lines) + "\n"
        with open(os.path.join(package_path, f"{name}Config.cmake.in"), "w") as f:
            f.write(content)

    create_relay_in(versioned_name)
    create_relay_in(unversioned_name)
    print(f"Patched {pkg_name} with relay templates")

if __name__ == "__main__":
    base_path = "src/gazebo-release"
    if os.path.exists(base_path):
        for pkg in os.listdir(base_path):
            full_path = os.path.join(base_path, pkg)
            if os.path.isdir(full_path) and pkg.endswith("_vendor"):
                patch_gz_vendor(full_path)
