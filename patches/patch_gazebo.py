import os
import re
import subprocess
import sys

def git_reset(path):
    pkg_dir = os.path.dirname(path)
    while pkg_dir and not os.path.exists(os.path.join(pkg_dir, '.git')):
        next_dir = os.path.dirname(pkg_dir)
        if next_dir == pkg_dir:
            pkg_dir = None
            break
        pkg_dir = next_dir
    
    if pkg_dir:
        rel_path = os.path.relpath(path, pkg_dir)
        subprocess.run(["git", "checkout", "--", rel_path], cwd=pkg_dir, capture_output=True)

def patch_gz_vendor(package_path):
    cmakelists_path = os.path.join(package_path, "CMakeLists.txt")
    if not os.path.exists(cmakelists_path):
        return

    git_reset(cmakelists_path)

    pkg_name = os.path.basename(package_path)
    pkg_basename = pkg_name.replace("_vendor", "").replace("_", "-")
    
    with open(cmakelists_path, "r") as f:
        content = f.read()
        match = re.search(r"set\(LIB_VER_MAJOR (\d+)\)", content)
        if match:
            major_version = match.group(1)
        else:
            if "cmake" in pkg_name: major_version = "3"
            elif "math" in pkg_name: major_version = "7"
            elif "utils" in pkg_name: major_version = "2"
            else: major_version = ""

    versioned_name = f"{pkg_basename}{major_version}"
    unversioned_name = pkg_basename

    # 1. Add ament_export_dependencies
    export_dep = f"ament_export_dependencies({versioned_name} {unversioned_name})\n"
    if "ament_export_dependencies" not in content:
        content = content.replace("ament_package()", export_dep + "ament_package()")
    
    # 2. Add logic to create and install relay Config files
    # Use EXPLICIT hardcoded names to avoid project name ambiguity
    
    def get_relay_content(name):
        lines = [
            f"# Generated by patch_gazebo.py for {name}",
            # We use the explicit pkg_name here
            f"set({pkg_name}_PREFIX \"${{{pkg_name}_DIR}}/../../../opt/{pkg_name}\")",
            f"list(APPEND CMAKE_PREFIX_PATH \"${{{pkg_name}_PREFIX}}\")",
            f"list(APPEND CMAKE_PREFIX_PATH \"${{{pkg_name}_PREFIX}}/share\")",
            "",
            f"set({versioned_name}_DIR \"${{{pkg_name}_PREFIX}}/share/cmake/{unversioned_name}\")",
            f"if(NOT EXISTS \"${{{versioned_name}_DIR}}\")",
            f"  set({versioned_name}_DIR \"${{{pkg_name}_PREFIX}}/lib/cmake/{unversioned_name}\")",
            "endif()",
            f"set({unversioned_name}_DIR \"${{{versioned_name}_DIR}}\")",
            "",
            f"find_package({unversioned_name} REQUIRED NO_DEFAULT_PATH)",
            "",
            f"if({unversioned_name}_FOUND)",
            f"  set({name}_FOUND ON)",
            f"  if(NOT TARGET {unversioned_name}::core AND TARGET {unversioned_name}::{unversioned_name})",
            f"    add_library({unversioned_name}::core ALIAS {unversioned_name}::{unversioned_name})",
            "  endif()",
            f"  if(NOT TARGET {name}::{name} AND TARGET {unversioned_name}::{unversioned_name})",
            f"    add_library({name}::{name} ALIAS {unversioned_name}::{unversioned_name})",
            "  endif()",
            "else()",
            f"  message(FATAL_ERROR \"Could not find {unversioned_name} in ${{{pkg_name}_PREFIX}}\")",
            "endif()"
        ]
        # Escape for CMake's file(WRITE)
        return "\\n".join(lines).replace('$', '\\$').replace('"', '\\"')

    relay_logic = f"""
file(WRITE "${{CMAKE_CURRENT_BINARY_DIR}}/{versioned_name}Config.cmake" "{get_relay_content(versioned_name)}")
install(FILES "${{CMAKE_CURRENT_BINARY_DIR}}/{versioned_name}Config.cmake" DESTINATION share/{versioned_name}/cmake)

file(WRITE "${{CMAKE_CURRENT_BINARY_DIR}}/{unversioned_name}Config.cmake" "{get_relay_content(unversioned_name)}")
install(FILES "${{CMAKE_CURRENT_BINARY_DIR}}/{unversioned_name}Config.cmake" DESTINATION share/{unversioned_name}/cmake)
"""
    if versioned_name + "Config.cmake" not in content:
        content = content.replace("ament_package()", relay_logic + "\nament_package()")

    with open(cmakelists_path, "wb") as f:
        f.write(content.encode('utf-8').replace(b'\r\n', b'\n'))
    
    print(f"Patched {pkg_name} with explicit installation-based relays")

if __name__ == "__main__":
    base_path = "src/gazebo-release"
    if os.path.exists(base_path):
        for pkg in os.listdir(base_path):
            full_path = os.path.join(base_path, pkg)
            if os.path.isdir(full_path) and pkg.endswith("_vendor"):
                patch_gz_vendor(full_path)
