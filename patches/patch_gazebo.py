import os
import re
import subprocess
import sys

def git_reset(path):
    pkg_dir = os.path.dirname(path)
    while pkg_dir and not os.path.exists(os.path.join(pkg_dir, '.git')):
        next_dir = os.path.dirname(pkg_dir)
        if next_dir == pkg_dir:
            pkg_dir = None
            break
        pkg_dir = next_dir
    
    if pkg_dir:
        rel_path = os.path.relpath(path, pkg_dir)
        subprocess.run(["git", "checkout", "--", rel_path], cwd=pkg_dir, capture_output=True)

def patch_gz_vendor(package_path):
    cmakelists_path = os.path.join(package_path, "CMakeLists.txt")
    if not os.path.exists(cmakelists_path):
        return

    git_reset(cmakelists_path)

    pkg_name = os.path.basename(package_path)
    pkg_basename = pkg_name.replace("_vendor", "").replace("_", "-")
    
    with open(cmakelists_path, "r") as f:
        content = f.read()
        match = re.search(r"set\(LIB_VER_MAJOR (\d+)\)", content)
        if match:
            major_version = match.group(1)
        else:
            if "cmake" in pkg_name: major_version = "5"
            elif "math" in pkg_name: major_version = "9"
            elif "utils" in pkg_name: major_version = "4"
            else: major_version = ""

    versioned_name = f"{pkg_basename}{major_version}"
    unversioned_name = pkg_basename

    # 1. Add/Update ament_export_dependencies
    # We MUST ensure versioned and unversioned names are exported
    names_to_export = [versioned_name, unversioned_name]
    if "ament_export_dependencies" in content:
        # Append to existing call
        for name in names_to_export:
            if name not in content:
                content = re.sub(r"(ament_export_dependencies\()", r"\1" + name + "\n  ", content)
    else:
        # Add new call
        export_dep = f"ament_export_dependencies({versioned_name} {unversioned_name})\n"
        content = content.replace("ament_package()", export_dep + "ament_package()")
    
    # 2. Add logic to create and install relay Config files
    
    def get_relay_content(name):
        lines = [
            "# Generated by patch_gazebo.py for " + name,
            "message(STATUS \"--- GZ RELAY: Loading " + name + " via " + pkg_name + " ---\")",
            "set(" + pkg_name + "_PREFIX \"${" + name + "_DIR}/../../../opt/" + pkg_name + "\")",
            "list(APPEND CMAKE_PREFIX_PATH \"${" + pkg_name + "_PREFIX}\")",
            "list(APPEND CMAKE_PREFIX_PATH \"${" + pkg_name + "_PREFIX}/share\")",
            "",
            "set(_gz_cfg_dir \"${" + pkg_name + "_PREFIX}/share/cmake/" + unversioned_name + "\")",
            "if(NOT EXISTS \"${_gz_cfg_dir}\")",
            "  set(_gz_cfg_dir \"${" + pkg_name + "_PREFIX}/lib/cmake/" + unversioned_name + "\")",
            "endif()",
            "",
            "set(_gz_cfg_file \"${_gz_cfg_dir}/" + unversioned_name + "-config.cmake\")",
            "if(EXISTS \"${_gz_cfg_file}\")",
            "  message(STATUS \"--- GZ RELAY: Including real config from ${_gz_cfg_file} ---\")",
            "  include(\"${_gz_cfg_file}\")",
            "  set(" + name + "_FOUND ON)",
            "  # Create aliases",
            "  if(NOT TARGET " + unversioned_name + "::core AND TARGET " + unversioned_name + "::" + unversioned_name + ")",
            "    add_library(" + unversioned_name + "::core ALIAS " + unversioned_name + "::" + unversioned_name + ")",
            "  endif()",
            "  if(NOT TARGET " + name + "::" + name + " AND TARGET " + unversioned_name + "::" + unversioned_name + ")",
            "    add_library(" + name + "::" + name + " ALIAS " + unversioned_name + "::" + unversioned_name + ")",
            "  endif()",
            "else()",
            "  message(FATAL_ERROR \"Could not find ${_gz_cfg_file}\")",
            "endif()"
        ]
        return "\\n".join(lines).replace('$', '\\$').replace('"', '\\"')

    relay_logic = "\n"
    relay_logic += "file(WRITE \"${CMAKE_CURRENT_BINARY_DIR}/" + versioned_name + "Config.cmake\" \"" + get_relay_content(versioned_name) + "\")\n"
    relay_logic += "install(FILES \"${CMAKE_CURRENT_BINARY_DIR}/" + versioned_name + "Config.cmake\" DESTINATION share/" + versioned_name + "/cmake)\n"
    relay_logic += "\n"
    relay_logic += "file(WRITE \"${CMAKE_CURRENT_BINARY_DIR}/" + unversioned_name + "Config.cmake\" \"" + get_relay_content(unversioned_name) + "\")\n"
    relay_logic += "install(FILES \"${CMAKE_CURRENT_BINARY_DIR}/" + unversioned_name + "Config.cmake\" DESTINATION share/" + unversioned_name + "/cmake)\n"

    if versioned_name + "Config.cmake" not in content:
        content = content.replace("ament_package()", relay_logic + "\nament_package()")

    with open(cmakelists_path, "wb") as f:
        f.write(content.encode('utf-8').replace(b'\r\n', b'\n'))
    
    print(f"Patched {pkg_name} with robust literal relays and exported dependencies")

if __name__ == "__main__":
    base_path = "src/gazebo-release"
    if os.path.exists(base_path):
        for pkg in os.listdir(base_path):
            full_path = os.path.join(base_path, pkg)
            if os.path.isdir(full_path) and pkg.endswith("_vendor"):
                patch_gz_vendor(full_path)
